    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)jTable2.getModel(); {
        if (jTable2.getSelectedRow()==-1){
            if (jTable2.getRowCount()==0) {
                JOptionPane.showMessageDialog(null, "Tidak ada data dalam table");
            } else {
                JOptionPane.showMessageDialog(null, "Pilih data yang akan diubah");
            }
        } else {
            model.setValueAt(txtid.getText(), jTable2.getSelectedRow(), 0);
            model.setValueAt(txtnama.getText(), jTable2.getSelectedRow(), 1);
            model.setValueAt(txtalamat.getText(), jTable2.getSelectedRow(), 2);
        }
    }
        reset();
    }//GEN-LAST:event_jButton3ActionPerformed

